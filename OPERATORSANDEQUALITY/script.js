const a = 10;
const b = 30;
let result = 0;

result = a + b;
console.log(result);

result = a - b;
console.log(result);

result = a / b;
console.log(result);

result = a * b;
console.log(result);

result = a ** b;
console.log(result);

result = a % b;
console.log(result);

let number = 5;
result = ++number;
console.log(result);


